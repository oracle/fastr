package com.oracle.truffle.r.test.packages.analyzer;

import java.nio.file.Path;
import java.util.Collection;

/**
 * Represents a tested R package.
 */
public class RPackage {

    private String name;
    private String version;
    private Path location;
    private Collection<RPackageTestRun> testRuns;

    protected RPackage(String name, String version) {
        this.name = name;
        this.version = version;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public Path getLocation() {
        return location;
    }

    public void setLocation(Path location) {
        this.location = location;
    }

    public Collection<RPackageTestRun> getTestRuns() {
        return testRuns;
    }

    public void setTestRuns(Collection<RPackageTestRun> testRuns) {
        this.testRuns = testRuns;
    }

    @Override
    public String toString() {
        return name + "-" + version;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((version == null) ? 0 : version.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        RPackage other = (RPackage) obj;
        if (name == null) {
            if (other.name != null) {
                return false;
            }
        } else if (!name.equals(other.name)) {
            return false;
        }
        if (version == null) {
            if (other.version != null) {
                return false;
            }
        } else if (!version.equals(other.version)) {
            return false;
        }
        return true;
    }

}
